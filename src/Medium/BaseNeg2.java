package Medium;
/*
1017. 负二进制转换
用户通过次数 156
给出数字 N，返回由若干 "0" 和 "1"组成的字符串，该字符串为 N 的负二进制（base -2）表示。
除非字符串就是 "0"，否则返回的字符串中不能含有前导零。
 */
public class BaseNeg2 {
    public static void main(String[] args){
        System.out.println(baseNeg2(4));
    }
    public static String baseNeg2(int N) {
        if (N==0)
            return "0";
        return "0";
    }
}
